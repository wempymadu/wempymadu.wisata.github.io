		<td valign="top" width="715"><h1>Pulau Gili Motang </h1>
		  <p>Gili Motang adalah pulau kecil di Indonesia, yang merupakan bagian dari gugusan kepulauan di dalam Taman Nasional Komodo. Pulau ini memiliki luas sekitar 30 km&sup2;. Pulau ini dihuni oleh sekitar seratus ekor biawak komodo (ora). Pada tahun 1991, Gili Motang bersama-sama dengan kepulauan lainnya di sekitar Pulau Komodo diterima sebagai Situs Warisan Dunia UNESCO.</p>
		  <p>Dari puncak bukit yang dikenal dengan Golo Kode, pengunjung dapat menyaksikan panorama dan bentang alam yang cukup fantastik karena keterwakilan berbagai tipe ekosistem dapat disaksikan dari tempat ini.</p>
