		<td valign="top" width="715"><h1>Pulau Rinca </h1>
		  <p>Loh Buaya merupakan pusat kunjungan wisatawan di Pulau Rinca. Pengunjung dapat menyaksikan hutan bakau, padang savana serta satwa liar misalnya komodo, rusa timor, kerbau liar, monyet ekor panjang, kuda liar serta berbagai jenis burung.</p>
		  <p>Aktivitas yang ditawarkan kepada pengunjung di Loh Buaya antara lain pengamatan satwa liar, penjelajahan (Loh Buaya - Wae Waso, Loh Buaya - Golo Kode), photo hunting, video shooting, pengamatan kalong di Pulau Kalong (depan Kampung Rinca) dan pengamatan batu balok di kampung Rinca.</p>
		  <p>Fasilitas yang tersedia di Loh Buaya antara lain pondok wisata, cafetaria, shelter dan jalan setapak.</p>
		  <p>Di Pulau Kalong, aktivitas yang dapat dilakukan antara lain pengamatan koloni kelelawar dalam jumlah yang cukup besar. Pengamatan paling menarik dilakukan pada saat sore hari ketika kelelawar mulai keluar untuk mencari makan.</p>
		  <p>Dari puncak bukit yang dikenal dengan Golo Kode, pengunjung dapat menyaksikan panorama dan bentang alam yang cukup fantastik karena keterwakilan berbagai tipe ekosistem dapat disaksikan dari tempat ini.</p>
